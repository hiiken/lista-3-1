idade = int(input());


if(idade > 90):
    print("Fevereiro")
elif(80 < idade < 90):
    print("Marco")
elif(70 <= idade <= 80):
    print("Abril")
elif(60 < idade <= 70):
    print("Maio")
elif(50 < idade <= 60):
    print("Junho")
elif(40 < idade <= 50):
    print("Julho")
elif(30 < idade <= 40):
    print("Agosto")
elif(20 < idade <= 30):
    print("Setembro")
else:
    print("Outubro")
